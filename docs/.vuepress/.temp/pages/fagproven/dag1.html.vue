<td style="text-align:left">Begrunnelse for tiltak:</td>
<td style="text-align:left">En person som har problem med å rekke fram til toalettet, kjenner vannlatingstrang. Tiden fra trangen kommer og til hun må tømme blæra, er kort, og det er vanskelig eller umulig å holde tilbake vannlating for pasienten. Ved å ha toalettet eller dostol/bekken i nærheten av pasienten, kan tiden som trengs for å komme på toalett, kortes ned. Fordi pasienten har fysisk styrke til å bevege seg, vil hun selv kunne komme seg på toalettet. Skulle hun trenge jhjkelp fordi toalettet i en situasjon er for langt unna, kan hun også si fra når behovet melder seg. I tillegg vil det hjelpe at pasienten har på seg klær som raskt kan tas av. Pasientens fysiske styrke vil også hjelpe i denne situasjonen.<br> Når det likevel kommer urin i bleie eller truse, må det skiftes raskt. Urin som ligger mot hud, vil bløte opp og irritere huden, slik at den lettere blir rød og sår. For å forebygge det må pasinten skifte til tørr bleie eller truse hver gang det er nødvendig. Hvis pasienten klarer å skifte selv og forstår betydningen av det, kan det være nok å legge til rette for å skifte og for å ivareta intimhygiene.</td>
</tr>
