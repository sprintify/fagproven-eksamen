<p>En spisestue, spiserom eller lignende gjør det enklere å legge til rette for positive opplevelser under måltidene enn om pasienten spiser alene. Uavhengig av hvor pasienten spiser, er det viktig å tilrettelegge det fysiske miljøet og andre forhold som fremmer trivsel, matglede og det sosiale samværet.<br>
