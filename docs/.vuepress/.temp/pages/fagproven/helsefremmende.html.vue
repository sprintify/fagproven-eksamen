<p><strong>A) Forklar begrepet helse</strong></p>
<p>Verdens Helseorganisasjon (WHO) definerer helse som &quot;En tilstand av fullkomment legemlig, sjelelig og sosial velvære og ikke bare fravær av sykdom.&quot; Denne definisjonen av helse er noe man kan strekke seg etter, men i det virkelig liv uoppnåelig. En mer jordnær tilnærming til forståelsen av helse kan være menneskers evne til å opprettholde eller bedre allmenntilstanden fysisk og psykisk gjennom livet.
I vårt land legger vi vekt på et helhetlig menneskesyn når vi vurdere helsen. Det betyr at vi ikke bare ser menneskets kropp med fysiske behov som mat og personlige hygiene, men også de psykososiale aspektene. For at et menneske skal oppleve god helse bør fysisk, psykisk, sosiale og åndelige og kulturelle behov være dekket.
Fysisk helse er knyttet til kroppen og de kroppslige funksjonene, mens psykisk helse har med tanker og følelser og hvordan vi mestrer våre liv i omgang med andre mennesker.
For å trives og ha det bra er det viktig å være sammen med andre mennesker og på den måten utvikle de sosiale ferdigheten som er nødvendig i et samfunn. Gode sosiale relasjoner gir trivsel og trygghet. Dette gir mennesker motivasjon og mening og er et viktig bidrag til en god helse og ett langt liv.</p>
<br>
<p><strong>B) Hva menes med ett aktivt liv, når du tenker på den brukergruppen du arbeider med nå?</strong></p>
<p>Det er et grunnleggende behov - for pasienter å bevege seg. Derfor er det viktig å ha gode rutiner for aktivitet hver dag på institusjonen. Dette behovet ivaretas best når pasienten får delta i daglige gjøremål og å tilrettelegge for aktiviteter.
For å ivareta pasienten på en bedre måte ble aktivitetsdosetten innført for noen år siden på omsorgssenteret for å stimulere og bidra til et mer aktivt liv på institusjon. Det lages en aktivitetsplan for hver enkelt pasient og alle aktiviteter som gjennomføres dokumenteres i rapportsystemet. Behovene for aktivitet er forskjellig fra pasient, men for eksempel tilbys pasientene å gå turer med ledsager eller sitte på terrassen for å trekke frisk luft. For andre kan aktivitet være å lese avisen og diskutere hva som skjer i samfunnet eller tegne, strikke, legge puslespill etc. Felles aktiviteter som yatzy, ballonglek eller spille kort sammen er andre eksempler. Noen har behov for mer tilrettelagt fysisk aktivitet som å bruke ergometer sykkel med TV skjerm.
Normalt kommer fysioterapeuten en gang i uken og for de som ønsker å delta får de dette tilbudet hvor målsettingen er å opprettholde de fysiske funksjoner gjennom fysisk trening.</p>
<br>
<p><strong>C) Hvordan kan du som helsefagarbeider fremme brukerens helse, mestringsfølelse og trivsel?</strong></p>
<p>Ved å gi pasienten selvbestemmelse og deltakelse i beslutninger som dreier seg om fysiske og psykiske spørsmål som har med pasientens tilstand å gjøre kan det være med på å motivere pasienten til å foreta valg som fremmer helsen. Ved å stimulere til brukermedvirkning utnyttes pasientens ressurser som igjen fremmer brukerens helse, bedrer mestringsfølelsen og øker velvære og trivsel.
Sammen kan helsearbeider og pasient sette mål for å løse daglige gjøremål som stell, kosthold, aktiviteter og sosialt samvær med andre pasienter. Alt det pasienten kan gjøre selv bør pasienten utføre selv. Det kan ta mer tid, men det er viktig å la pasienten få tid til å utføre sine oppgaver. Her gjelder det å skynde seg langsomt dette for å opprettholde pasientens funksjonsnivå og mestringsfølelse. Det motsatte ved å hjelpe for mye kan tvert imot føre til passivitet og resignasjon over egen fysisk og psykisk kapasitet.
