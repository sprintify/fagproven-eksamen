<h5 id="a-hva-er-feilernæring" tabindex="-1"><a class="header-anchor" href="#a-hva-er-feilernæring" aria-hidden="true">#</a> A) Hva er feilernæring?</h5>
<p>Feilernæring innebærer at vi får for lite eller for mye av noen næringsstoffer, for eksempel vitaminer eller mineraler som f.eks jern. For mye tilført jern kan føre til jernforgiftning som kan være skadelig for helsen. Risikoen for feilnæring øker når mer enn 10 % av energien kommer fra raffinert sukker. Slik feilernæring kan føre til at det blir for lite jern eller andre mineraler og vitaminer i kostholdet. For å unngå feilernæring er det viktig å inneha et variert og sunt kosthold.
<br><br></p>
<h5 id="b-hva-er-underernæring" tabindex="-1"><a class="header-anchor" href="#b-hva-er-underernæring" aria-hidden="true">#</a> B) Hva er underernæring?</h5>
<p>Underernæring er en tilstand som oppstår når kroppens behov for energi og/eller næringsstoffer over tid ikke tilfredsstilles gjennom inntak av mat og drikke. Protein- og fettreservene i kroppen avtar som resulterer i vekttap, muskelsvinn og mangel på vitaminer og mineraler.
<br><br></p>
<h5 id="c-hvordan-kan-du-kartlegge-under-feilernæring" tabindex="-1"><a class="header-anchor" href="#c-hvordan-kan-du-kartlegge-under-feilernæring" aria-hidden="true">#</a> C) Hvordan kan du kartlegge under/feilernæring?</h5>
<p>Det kan være behov for å kartlegge pasientens matinntak dersom det er risiko for å utvikle underernæring/feilernæring. Dette dokumenteres gjennom en kostregistrering. Man kan også snakke med pasienten om pasientens spisevaner, preferanser og behov for tilpasninger av kosten.</p>
<p>Det viktigste tiltaket er å måle vekten etter faste tidspunkter f.eks. 1 gang i uken for å danne seg en objektiv metode for å fastlå om pasienten går opp eller ned i vekt eller at vekten er stabil over tid. Videre er det vanlig å måle kroppsmasseindeks (KMI) som er en metode for å klassifisere hvor man ligger på vektskalaen f.eks at man er for tynn eller overvektig, men her må man benytte en viss form for skjønn pga. kroppsbygning etc.
