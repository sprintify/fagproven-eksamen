<p><em>Kunne praktisere kartleggingsverktøyet MUST</em></p>
<p>Nasjonale faglige retningslinjer for forebygging og behandling av underernæring krever at alle pasienter skal vurderes for ernæringsmessig risiko (Helsedirektoratet 2009). Vurdering og dokumentasjon av ernæringsmessig risiko gjennomføres med standardiserte kartleggingsverktøy, som omfatter kartlegging av matinntak, vekt, høyde og KMI, samt vurdering vektendringer og andre ernæringsrelaterte data.</p>
<p>Overvekt og fysisk inaktivitet er økende i befolkningen og risikofaktorer for utvikling av både somatiske og psykiske tilleggslidelser. Fedme (KMI over 30) er et alvorlig helseproblem. Noen medikamenter kan medføre vektøkning, spesielt psykofarmaka. Helsedirektoratet har derfor utviklet Nasjonale retningslinjer for primærhelsetjenesten for Forebygging, utredning og behandling av overvekt og fedme hos voksne (2011).
<br><br></p>
