<p><em>Og hvorfor måler vi blodtrykk?</em><br>
Høyt blodtrykk (hypertensjon) betyr at trykket i kroppens pulsårer er høyere enn det som defineres som normalt. Det er en risikofaktor for utvikling av hjerneslag og hjerteinfarkt. Høyt blodtrykk gir vanligvis ingen symptomer. Ved betydelig forhøyet trykk, noe som er sjeldent, kan man imidlertid oppleve svimmelhet og hodepine.</p>
<p>Normalt blodtrykk ligger på 80/120 (80 er undertrykk og 120 er overtrykk).</p>
<p>Lavt blodtrykk kan være et tegn på at man for eksempel har fått i seg for lite drikk, en reaksjon fra nervesystemet, et tegn på sykdom eller kan stamme fra en alvorlig skade, blødning eller sjokk. Lavt blodtrykk ligger på under 80 og nedover.</p>
<h5 id="b-male-kroppstemperatur" tabindex="-1"><a class="header-anchor" href="#b-male-kroppstemperatur" aria-hidden="true">#</a> B) Måle kroppstemperatur</h5>
<p>Kroppens temperatur er en god indikator på sykdom som en infeksjonssykdom. Feber defineres som temperatur over 38°C målt i endetarmen.</p>
<p>Bestemmelse av kroppstemperatur er en viktig opplysning når en skal vurdere en syk person. Påvisning av infeksjoner hviler i høy grad på korrekt registrering av kroppstemperaturen.</p>
<p><strong>Feber</strong><br>
Den normale kroppstemperatur avhenger av målestedet og varierer fra 37,1ºC i endetarmen til 36,6ºC i øret og 36,4ºC i munnen. Over døgnet kan temperaturen variere noe med laveste temperatur om morgenen og høyeste om kvelden. Eggløsning og fysisk aktivitet kan forhøye kroppstemperaturen med cirka 0,2o–0,5oC.</p>
<p>Måling av temperaturen i endetarmen eller øret er de mest benyttede metoder. Endetarmsmålingene er de mest pålitelige. Andre metoder som ikke anbefales, er:</p>
<ul>
<li>Pannetermometer er en ny målemetode der kjernetemperaturen beregnes ut fra hudtemperaturen ved tinningpulsåren. Metoden kan i følge studier ikke anbefales.</li>
<li>Munnhuletermometer brukes mye f.eks. i USA, men mindre i Skandinavia. Målingene er vist å være mer unøyaktige enn målinger i endetarmen. Måles temperaturen i munnen, ligger verdiene ca 1/2 grad under verdiene fra endetarmen.</li>
<li>Måling av temperaturen i armhulen. Er unøyaktig og bør unngås, men anvendes en del hos barn.</li>
<li>Øremålinger kan også bli påvirket av ytre faktorer og gir heller ikke presise mål.</li>
</ul>
<p>Ved ekstreme kroppstemperaturer kan perifere temperaturmålinger (i øret, over tinningpulsåren, i armhulen eller i munnen) være så mye som 1 til 2 grader høyere eller lavere enn hva den faktiske kroppstemperaturen er.</p>
