Hva som gjør et måltid til en god opplevelse, varierer fra person til person. Som helsefagarbeider må vi være både kreativ og oppmerksom på samme tid. Det er viktig å spørre pasienten hva de ønsker å spise og hvor mye. Dessuten er det viktig å ta hensyn til pasientens behov særlig når det gjelder sykdomsbilde som f.eks. mat allergier. Ikke minst er det å presentere maten på en delikat måte noe som kan bidra til å bedre appetitten og opprettholde den gode helsen.</p>
