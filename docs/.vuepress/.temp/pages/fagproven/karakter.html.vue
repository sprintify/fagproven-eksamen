<h2 id="ikke-bestatt" tabindex="-1"><a class="header-anchor" href="#ikke-bestatt" aria-hidden="true">#</a> Ikke bestått?</h2>
<p>Fagprøven har tre utfall: <strong>Bestått meget godt, bestått</strong> eller <strong>ikke bestått.</strong></p>
<p>Har du fått «ikke bestått» på fag- eller svenneprøven kan du  klage på prøven.</p>
<p><em>Du kan klage på to grunnlag:</em></p>
<ol>
<li>Formelle feil eller ikke-faglige feil</li>
<li>Den faglige vurderingen</li>
</ol>
<p><em>Klage på fag- og svenneprøve skal sendes til Viken Fylkeskommune og må:</em></p>
<ul>
<li>Fremsettes skriftlig</li>
<li>Undertegnes av klageren eller av en med fullmakt</li>
<li>Vise til hva det klages på og inneholde en begrunnelse</li>
<li>Om nødvendig gi opplysninger som gjør et mulig å avgjøre om klageren har rett til å klage, og om klagefristen er overholdt</li>
</ul>
<p>Klagefristen er tre uker fra vedtaket er mottatt av lærlingen, eller burde vært kjent med vedtaket.</p>
<p>Dersom det er behov for særskilt tilrettelegging av fag- eller svenneprøven, må søknad om særskilt tilrettelegging av fag-svenneprøve med sakkyndig dokumentasjon sendes inn sammen med oppmelding.</p>
