<p><strong>Taushetsplikt</strong><br>
Helsepersonell har taushetsplikt. De kan kun formidle helseopplysninger om deg til andre hvis du har gitt samtykke til det, eller det er bestemt i lov at de skal eller kan, som for eksempel når det er nødvendig å sikre deg forsvarlig helsehjelp.</p>
<p><em>Taushetsplikten betyr blant annet at helsepersonell:</em></p>
<ul>
<li>både har plikt til å tie selv og til aktivt å hindre at uvedkommende får tilgang til helseinformasjonen din.</li>
<li>er bundet av taushetsplikten for alle opplysninger de mottar i egenskap av å være helsepersonell.</li>
<li>er bundet av taushetsplikten også etter at de har sluttet i jobben.</li>
<li>heller ikke kan utlevere opplysninger til politiet uten pasientens samtykke så lenge det ikke er fare for liv og helse.​</li>
</ul>
<p><strong>Personvern</strong><br>
Personvern er den enkelte menneskets ukrenkelighet og krav på respekt fra andre mennesker, respekt for egen integritet og privatlivets fred sentralt. Personvern er derfor nært knyttet til enkeltindividers muligheter for privatliv, selvbestemmelse og selvutfoldelse.</p>
<p>Dagens arbeids- og velferdssamfunn håndterer en stor mengde opplysninger om personlige forhold. Det er viktig at brukerne kan ha tillit til at opplysningene ikke spres til uvedkommende og ikke brukes til andre formål enn de er avgitt for.</p>
<p>Et vesentlig element i personvernet er at den enkelte skal ha kontroll over og i størst mulig grad kunne bestemme over egne personopplysninger, herunder ha rett til å få vite hvilke opplysninger andre kjenner til om en selv og hva de brukes til. Dette omtales ofte som personopplysningsvern, og det er primært denne dimensjonen som er underlagt omfattende lovregulering gjennom blant annet personopplysningsloven, helseregisterloven og regler om taushetsplikt. Men også personvern mer generelt er underlagt rettslig beskyttelse, for eksempel gjennom straffelovens regler om vern av privatlivets fred.</p>
<p>De reglene som skal sikre dette, er først og fremst personopplysningsloven og lovbestemmelser om taushetsplikt.
<br><br></p>
