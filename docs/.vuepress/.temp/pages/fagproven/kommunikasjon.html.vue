<p><strong>Brudd på taushetsplikten</strong><br>
Dersom du mener helsepersonell har brutt taushetsplikten, kan du be om en vurdering fra Statsforvalteren i din region. Statsforvalteren vil da hente inn nødvendig informasjon for å vurdere om helsepersonellet har opptrådt i tråd med taushetsplikten. Hvis Statsforvalteren mener at det foreligger alvorlig brudd på taushetsplikten, skal saken oversendes Statens helsetilsyn.​ Statsforvalter er det som tidligere het Fylkesmann.</p>
<p>Hvis Statens helsetilsyn kommer til at helsepersonellet har brutt taushetsplikten, kan de få en advarsel. Ved grove brudd, eller dersom helsepersonellet ikke innretter seg etter å ha fått advarselen, kan Statens helsetilsyn kalle tilbake helsepersonellets autorisasjon, lisens eller spesialistgodkjenning.</p>
