<p><strong>B) Hva er avviksmelding og når anvendes dette?</strong><br>
Virksomheten skal ha et avvikssystem der vold og trusler i forbindelse med arbeidet skal registreres. Arbeidsgiver skal sørge for at alle personskader som oppstår under utførelse av arbeid blir registrert. Dette registeret skal være tilgjengelig for verneombudet, arbeidsmiljøutvalget, bedriftshelsetjenesten og Arbeidstilsynet.</p>
<p>Arbeidstakere må rapportere hendelser inn i avvikssystemet slik at arbeidsgiver får oversikt over omfang og type hendelser som de ansatte utsettes for. Det er viktig informasjon å ha når man skal jobbe forebyggende med vold og trusler.</p>
<p>I bransjer der vold og trusler er en del av hverdagen, er arbeidstakerne ofte kjent med farene og har trening i å håndtere dem (f.eks. vernepleiere). Mange arbeidstakere i utsatte bransjer unnlater dermed å rapportere til arbeidsgiver fordi de anser vold og trusler for å være en del av arbeidet. Det kan være usikkerhet knyttet til hvilke hendelser som bør ansees som et «avvik» og dermed registreres. Arbeidstakere som ikke har klart å forhindre en hendelse, kan få skyldfølelse og dermed unnlate å rapportere hendelsen av den grunn.</p>
<p><strong>Eksempel på avvik</strong><br></p>
<p><em>Årsaken til uønsket hendelser skal registreres som avvik:</em></p>
<ul>
<li>Årsaken til alvorlige hendelser eller ulykker som har ført til skader på menneske, miljø eller utstyr.
<ul>
<li>Eksempel er årsaker til arbeidsulykker og arbeidsrelatert sykdom, skader på materiell og miljø, brann og miljø- eller helseskadelige utslepp.</li>
</ul>
</li>
</ul>
<p><em>Andre hendelser er kanskje ikke like synlige, men bør likevel registreres som avvik:</em></p>
<ul>
<li>Årsaker til nestenulykker som avverget en farlig situasjon. Det kan være feil på sikkerheitskritisk utstyr som blir oppdaget i tide, en åpen ventil som blir lukket, eller dårlig emballasje som må skiftes ut før det blir lekkasje.</li>
</ul>
<p><em>Eksempler på avvik på en helseinstitusjon kan være:</em></p>
