<p><strong>Diagnose</strong><br>
EKSEMPEL:<br>
Pasienten har diagnosen KOLS (kronisk obstruktiv lungesykdom). Lungefunksjonen er nedsatt på grunn av emfysem (ødelagte lungeblærer). Det er påvist innsnevring av luftveiene og pasienten er tungpustet.</p>
</li>
<li>
<p><strong>Årsak:</strong><br>
Sykdommen har utviklet seg gradvis på grunn av røyking over mange år. Pasienten har sluttet å røyke pga av den alvorlige diagnosen KOLS.</p>
</li>
<li>
<p><strong>Symptomer:</strong><br>
Piping i brystet, redusert fysisk yteevne, tungpustet og pasienten blir angstpreget når pusteproblemene oppstår.
