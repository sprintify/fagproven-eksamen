<h5 id="a-hva-menes-med-brukermedvirkning-gi-eksempler-pa-dette" tabindex="-1"><a class="header-anchor" href="#a-hva-menes-med-brukermedvirkning-gi-eksempler-pa-dette" aria-hidden="true">#</a> A) Hva menes med brukermedvirkning? Gi eksempler på dette.</h5>
<p>Hvis man er mottaker av helsetjenster fra hjemmetjensten/sykehjem er brukermedvirkning en lovfestet rettighet som betyr at beboer/pasient har krav på å være med å bestemme.</p>
<p>Brukermedvirkning i helsetjenesten handler om at brukere, pasienter og pårørende inngår i et samarbeid med fagpersoner på ulike nivåer. Det innebærer å inkludere brukere i beslutningsprosesser knyttet til egen helse, utvikling av tjenester og tilbud eller innen styring på overordnet nivå.</p>
<p>For brukeren innebærer det å bli tatt på alvor og møtt med respekt. For fagpersoner innebærer det å anerkjenne at brukerne har kompetanse som er av betydning og som det er viktig å lytte til.</p>
<p>Med brukermedvirkning betraktes brukeren som en likeverdig partner i diskusjoner og beslutninger som angår hans eller hennes problem.</p>
<p>Å lytte til dem som er til stede og nærværende ligger til grunn også for brukermedvirkning i ­helsetjenesten. Helsehjelp angår først og fremst pasienter og pårørende, og helsepersonell skal ivareta deres interesser. Det er pasientene som erfarer sykdom på kropp og sinn, og det er pasienter og pårørende som må leve med konsekvensene av ulike behandlingsvalg. Det er derfor viktig at helsepersonell lytter til deres innsikt og erfaringer. Det er pasientene som må være i sentrum.</p>
<p>Helsekompetanse er en forutsetning for brukermedvirkning. For at ­pasientene skal kunne delta i beslutninger om behandlingsvalg, må de ha innsikt i og kunnskap om konsekvenser av ulike valg. Behovet for helsekompetanse er nødvendig fordi moderne medisin blir stadig mer kompleks og komplisert (Helse- og omsorgsdepartementet 2019).</p>
<p>Myndighetenes retorikk er kraftfull og positiv i troen på mulighetene som ligger i å styrke helsekompetanse. Sårbare gruppers forutsetninger blir i mindre grad problematisert. Det kan handle om fysiske, psykiske, kognitive og sosiale begrensninger.</p>
<p>Pasienter er forskjellige, og så vel informasjon som beslutningsprosesser må tilpasses den enkeltes ønsker og behov. i en helsetjeneste preget av standardisering, normering og pakkeforløp, må det også være plass for persontilpasset medvirkning.</p>
<p>Brukermedvirkning handler i stor grad om overføring av makt fra helse­institusjoner og helsepersonell til pasienter og brukere. Dette innebærer også makt til å definere behov. Dette kan ha bidratt til å utvide helsetjenestens arbeidsområde i tro på at økt aktivitet fra helseinstitusjoner og helsepersonell vil bedre helsen.</p>
<p>Det er viktig at helsepersonell har kunnskap om hva, hvordan og når pasienten og pårørende bør delta i helsehjelp. Ved akutt sykdom ønsker for eksempel pasienter i liten grad å medvirke. ­Kronisk sykdom krever derimot en annen tilnærming både til pasient og pårørende. Dette viser at brukermedvirkning vil variere i et pasientforløp.</p>
<p>Målet bør være en styrket pasientrolle og med det økt pasientmedvirkning.
<br><br></p>
<p><em><strong>Eksempel på brukermedvirkning:</strong></em></p>
<p>En dement pasient uten nære relasjoner får tilbud om sykehjemsplass etter at hjemmetjenesten har konkludert med at det er uforsvarlig for beboeren og å fortsette å bo i deres boenhet. Først blir pasienten innkalt til innkomstsamtale på sykehjemmet med en sykepleier fra hjemmetjenesten som kjenner vedkommende godt.</p>
<p>Pasienten blir vist rundt sykehjemmets ulike avdelinger og det blir gitt informasjon om de ulike yrkesgruppene og hvordan de samarbeider for at pasienten skal ha det bra på sykehjemmet og at tilsynslegen har legevistt hver uke.</p>
<p>Sykehjemmet informerer at pasientene har få, men faste personer å forholde seg til. Hver pasient har en sykepleierkontakt og en primærkontakt.</p>
<p>Under samtalen blir pasienten spurt om det er noen ønsker han har, med hensyn til rom, mat eller interesser som de bør ta hensyn til. Pasienten begynner å fortelle om sin egen situasjon og de tilstedværende lytter aktivt og viser pasienten respekt.</p>
<p>Det er tydelig at pasienten føler seg ivaretatt og føler seg tryggere etter dette møtet og takker ja til sykehjemsplassen.</p>
<p>Siden denne pasienten kom til innkomstsamtale tidlig i sitt sykdomsforløp var det vesentlig enklere å benytte stor grad av brukermedvirkning, men det vanlige er at pasientene i større grad kommer til sykehjem langt senere. Da ville denne personen i mindre grad vært i stand til selv å bestemme over sine valg. Da blir det ofte pårørende eller oppnevnt verge som må ta den endelige beslutningen og mennesket rett til brukermedvirkning og autonomi blir begrenset.
<br><br></p>
<h5 id="b-hvorfor-er-det-viktig-a-legge-til-rette-for-samarbeid-med-parørende-kan-du-gi-eksempel-pa-dette-ved-ditt-lærested" tabindex="-1"><a class="header-anchor" href="#b-hvorfor-er-det-viktig-a-legge-til-rette-for-samarbeid-med-parørende-kan-du-gi-eksempel-pa-dette-ved-ditt-lærested" aria-hidden="true">#</a> B) Hvorfor er det viktig å legge til rette for samarbeid med pårørende? Kan du gi eksempel på dette ved ditt lærested?</h5>
<p>God informasjon og veiledning fra helsepersonell til pårørende kan være avgjørende, både for pårørendes egen helse, og for hvordan omsorgsrollen oppleves og utføres. Pårørende kan spille en viktig rolle i behandling og pleie av pasienter spesielt med de som har psykososiale problemstillinger f.eks. demens og parkinsons sykdommer.</p>
<p>Pårørende har ofte kunnskap som er viktig for vurdering av en pasients psykiske og fysiske tilstand og om sosiale, kulturell og åndelig forhold eller andre interesser pasienten har. De kjenner ofte personen langt bedre enn helsepersonellet, og vet hvordan personen er når han eller hun omgås andre.</p>
<p>Kjennskap til bakgrunn, omgivelser og ressursene til den som er pasient gjør ofte pårørende til en viktig samarbeidspart for helsepersonellet. Samtidig kan det å være pårørende til noen pasienter med f.eks. en demens diagnose oppleves som en belastning. Derfor er det svært viktig for helsepersonell og forklare sykdomsforløp og hvilken tilnærming som vil være best mulig for pasienten.</p>
<p>Pårørende må dessuten ha kunnskap om hva sykehjemmet kan tilby, så det ikke skapes urealistiske forventninger. En god relasjon innebærer også at pårørende respekterer og verdsetter personalet og dets kompetanse, og at de anerkjenner den relasjonen personalet etablerer til pasienten.
<br><br></p>
<p><em><strong>Eksempel samarbeid med pårørende:</strong></em></p>
<p>En pasient har nylig fått fast plass på sykehjemmet etter at det ikke var mulig for hennes datter og ta vare på sin mor hjemme siden hun skulle gjennom flere operasjoner på sykehuset. Datteren ble innkalt til pårørende møte for å få informasjon om moren som var nylig innlagt  på sykehjemmet. Sykepleieren forteller da at tilsynslegen har gjennomført kognitive tester på moren og kommet frem til at moren var moderat dement.</p>
<p>Datteren protesterte kraftig på diagnosen og sa at hennes mor bare var litt glemsk nå og da. Etter at sykepleier forklarte datteren hva denne testen innebar ble datteren roligere og etterhvert gikk det opp for henne at disse testresultatene stemte med virkeligheten og at hun som nær pårørende ikke hadde klart å vurdere sin egen mors situasjon. For nære relasjoner kan det være vanskelig å ta innover seg virkeligheten derfor er pårørende samtaler svært viktig for nettopp å klargjøre fakta. Derfra kan man sette nye mål og tiltak som kan opprettholde eller bedre pasientens funksjonsnivå og gi god omsorg og pleie.
<br><br></p>
<p><strong>TILLEGGSINFORMASJON OM BRUKERMEDVIRKNING</strong></p>
<p>Norske kommuner har rettet lite oppmerksomhet mot brukermedvirkning innenfor demensomsorgen. Pårørende kan ha påtatt seg ansvar for pasienten i hjemmet over tid. Ofte meldes eller registreres behovet for hjelp sent i sykdomsforløpet. Når pasienten først får tilbud om sykehjemsplass, er behovet for hjelp kommet langt. Ved innkomstsamtaler i sykehjem innkalles som regel ikke pasienten selv, men de pårørende. Pasienten får da ikke anledning til å uttrykke egne ønsker og behov ved en sykehjemsplassering. Det er tendenser til holdninger som ekskluderer pasienter både med og uten samtykkekompetanse fra å delta mer aktivt i tjenestene som mottas.</p>
<p>Oppmerksomheten har vært knyttet mest til brukermedvirkning for pårørende. Brukermedvirkning i helsepolitikken skjer på systemnivå, men det er særlig i den individuelle tilpasningen, i relasjonen mellom brukere og kommunale tjenesteytere, at det er store behov. Det er nødvendig å arbeide mer systematisk med å implementere brukermedvirkning i demensomsorgen på individuelt plan i kommunene.</p>
<p>Helsedirektoratet beskriver begrepet på følgende måte: (kilde Helsedirektoratet)</p>
<p>Brukere har rett til å medvirke, og tjenestene har plikt til å involvere brukeren. Samtidig har brukermedvirkning en egenverdi, terapeutisk verdi og er et virkemiddel for å forbedre og kvalitetssikre tjenestene. Brukermedvirkning innebærer at brukeren betraktes som en likeverdig partner i diskusjoner og beslutninger som angår hans eller hennes problem.</p>
<p><strong>Brukermedvirkning på tre nivå</strong></p>
<p>Brukermedvirkning skal skje på flere nivå, og nivåene reguleres av ulike lovverk:</p>
<ul>
<li>Individnivå</li>
<li>Tjeneste-/systemnivå</li>
<li>System-/politisk nivå</li>
</ul>
<p>Individnivå reguleres av Lov om pasient- og brukerrettigheter , og gjelder uavhengig av om tjenestene tilbys i kommune- eller spesialisthelsetjenesten. Loven regulerer blant annet retten til informasjon og retten til å medvirke ved gjennomføring av helse- og omsorgstjenester. Det betyr at den som benytter seg av et tjenestetilbud skal medvirke i valg, utforming og anvendelse av de tilbud som til enhver tid måtte være tilgjengelige, noe som igjen vil innebære økt mestring og kontroll over eget liv.</p>
<p>Retten til individuell plan ved behov for langvarige og koordinerte tjenester hjemles også i denne loven. Tjenestene skal ta utgangspunkt i brukerens egne mål. Fagkompetansen og brukerkompetansen utfyller hverandre.</p>
