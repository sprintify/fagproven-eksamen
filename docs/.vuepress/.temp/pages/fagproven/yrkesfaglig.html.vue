<h5 id="a-definer-hva-tvang-er-og-beskriv-eksempler-pa-nar-tvang-er-lov-a-bruke" tabindex="-1"><a class="header-anchor" href="#a-definer-hva-tvang-er-og-beskriv-eksempler-pa-nar-tvang-er-lov-a-bruke" aria-hidden="true">#</a> A) Definer hva tvang er og beskriv eksempler på når tvang er lov å bruke!</h5>
<p><strong>Definisjon av tvang:</strong></p>
<p>Reglene har som formål å hindre at pasienten utsetter seg selv, andre eller gjenstander for vesentlig skade og å begrense bruk av tvang og makt. Som tvang og makt regnes tiltak som pasienten motsetter seg, eller som er så inngripende at de uavhengig av motstand må regnes som tvang eller makt.
<br><br></p>
<p><strong>To sentrale vilkår er at:</strong></p>
<ul>
<li>pasienten må mangle samtykkekompetanse</li>
<li>tvangen er til pasientens beste
<br><br></li>
</ul>
<p><strong>Sentrale vilkår for bruk av tvang etter kapittel 4A i pasient- og brukerrettighetsloven (5 – 7)</strong></p>
<ul>
<li>Pasienten må mangle samtykkekompetanse</li>
<li>Samtykkekompetanse skal vurderes av den som er faglig ansvarlig for helsehjelpen</li>
<li>Tillitsskapende tiltak må være forsøkt</li>
<li>Helsehjelpen må være somatisk (psykisk helsehjelp reguleres av annet lovverk)</li>
<li>Unnlatelse av å gi helsehjelp kan føre til vesentlig helseskade for pasienten</li>
<li>Helsehjelpen må være nødvendig</li>
<li>Tiltakene må stå i forhold til behovet for helsehjelpen</li>
<li>Å yte helsehjelpen med tvang må være den klart beste løsningen for pasienten</li>
<li>Vedtak om bruk av tvang skal fattes av den som er ansvarlig for helsehjelpen</li>
<li>Somatisk helsehjelp mot motstand kan bare gis av hensyn til pasienten selv</li>
<li>Alvorlige inngrep, herunder inngrep i kroppen, reseptbelagte medikamenter og bevegelseshindrende tiltak, kan kun besluttes etter samråd med annet kvalifisert helsepersonell</li>
<li>Tvang skal aldri brukes som følge av manglende ressurser
<br><br></li>
</ul>
<p><strong>Hva er samtykkekompetanse?</strong></p>
<p>Samtykkekompetanse er ikke definert i loven, men veldig enkelt kan det defineres som evnen til å ta en meningsfull beslutning.
<br><br><br></p>
<p><strong>EKSEMPLER PÅ SITUASJONER DER MAN BRUKTE TVANG</strong></p>
<p><em>Tvang blir gjerne benyttet i situasjoner der pasienten yter fysisk eller verbal motstand mot behandling eller stell som er til pasientens beste.</em>
<br><br></p>
<p><strong>Motstand mot medisinering (både fysisk og verbal motstand)</strong></p>
<ul>
<li>legemidler (f.eks. beroligende medisiner, andre psykofarmaka, antibiotika og insulin), medisiner skjult i maten (f.eks. i syltetøyet) eller gitt med sprøyte i munnen eller intramuskulært
<br><br></li>
</ul>
<p><strong>Motstand mot stell, pleie og rehabilitering</strong></p>
<ul>
<li>på/avkledning, løftesituasjoner og forflytning ved stell, sårskift, hygiene, munnstell, dusjing, hårvask, negleklipping, gå på toalettet, bruk av bleie/bleieskift, buksedress/body (for å holde bleie på plass/unngå grising med urin og avføring), rehabilitering og fysisk aktivitet, holde pasientens hender under stell for å unngå å bli slått
<br><br></li>
</ul>
<p><strong>I forbindelse med ernæring og ernæringsbehandling</strong></p>
<ul>
<li>gi mat og drikke når pasienten ikke ønsker det, begrensning av matinntak med tvang for å unngå overspising, gi kunstig ernæring eller væske (intravenøst eller med sonde) til tross for motstand
<br><br></li>
</ul>
<p><strong>Motstand mot diagnostikk og invasive prosedyrer</strong></p>
<ul>
<li>ta blodprøver og utføre kateterisering
<br><br></li>
</ul>
<p><strong>Ved forebygging av skade, fare o.l.</strong></p>
<ul>
<li>bevegelseshindre (for eksempel sengehest og brett på stol for å hindre at pasienten reiser seg og faller), hindre pasienten å rote og grise på institusjonskjøkkenet eller skru på ovner m.m.
<br><br></li>
</ul>
<p><strong>Innleggelse og tilbakeholdelse</strong></p>
<ul>
<li>innleggelse fra hjemmet, tilbakeholdelse i sykehjemmet (for eksempel låste dører, skjule heisknappene med et papir, skru ned vindushåndtak fordi pasienten ellers hopper ut) f.eks. ved ønske om å få gå ut eller gå/flytte hjem og i avskjedssituasjoner (vil følge med pårørende)
<br><br></li>
</ul>
<p><strong>I forbindelse med daglige aktiviteter</strong></p>
<ul>
<li>styring av døgnrytme (legge seg/stå opp), slå av tv-en, hvem som skal sitte sammen med hvem i ulike sosiale situasjoner, gå tur/utetid (noen vil være mer ute eller gå mer enn bemanningen tillater), deltakelse i aktiviteter og tid alene (noen vil ikke delta i noe som helst og bare ligge i senga), skyve pasienten dit pasienten ikke vil
<br><br></li>
</ul>
<p><strong>Ved uønsket atferd, uro og vandring</strong></p>
<ul>
<li>hindre atferd som langvarig roping, akutt forvirring, grising med avføring, pakking av klær for å dra hjem (blir ofte mye rot), rote i egne og andres skap eller eiendeler, plukke ut/ned ting for å pakke/flytte, spytting på de ansatte, utskjelling, mobbing, sjikanering, rasisme, trusler, truende atferd eller vold mot de ansatte og andre pasienter (slag, spark, skalling, biting, kloring, klyping, spytting), gå inn på andre pasienters rom og rote, vekke, plage, rive ut slanger/medisinsk utstyr (for eksempel smertepumpe, kateter), rasering/ødeleggelse av inventar, persienner, vinduer og gjerde etc. (f.eks. fordi pasienten ikke skjønner hvordan man går ut på mindre ødeleggende måter)
<br><br><br></li>
</ul>
<p><strong>EKSEMPLER PÅ TVANG SOM BENYTTES, MEN SOM IKKE ER LOVHJEMLET I KAPITTEL 4A.</strong>
<br><br>
<em>Tvang ble imidlertid brukt også av hensyn til andre, f.eks. medpasienter, pårørende og ansatte.</em>
<br><br></p>
<p><strong>Tvang av hensyn til andre</strong></p>
<ul>
<li>avlastningsopphold for å avlaste ektefelle/pårørende, nekte en pasient å ringe (fordi vedkommende plager pårørende eller ringer ofte til legevakten eller 113 uten noen egentlig grunn), bruk av løfteanordninger (takheis) for å spare rygger og helse til de ansatte
<br><br></li>
</ul>
<p><strong>Tvang pga. mangel på ressurser</strong></p>
<ul>
<li>bruk av beroligende medisiner eller frihetsberøvelse i stedet for aktiviteter, stabilt personell med nødvendig kompetanse og sosial kontakt
<br><br></li>
</ul>
<p><strong>Tvang som ikke tilfredsstiller formale krav</strong></p>
<ul>
<li>ikke fattet vedtak, feil utfylt vedtak, feil person vedtar eller vurderer samtykkekompetanse
<br><br></li>
</ul>
<p><strong>Ulovlig frihetsberøvelse</strong></p>
<ul>
<li>låste dører (f.eks. på en skjermet avdeling) uten enkeltvedtak på hver pasient, tilbakeholdelse i omsorgsbolig
<br><br></li>
</ul>
<p><strong>Tvangsbehandling for psykisk lidelse (uten vedtak etter lov om psykisk helsevern)</strong></p>
<ul>
<li>atferdsforstyrrelser ved demens, uro, aggresjon, akutt forvirring (dette er psykiske lidelser ifølge ICD-10)
<br><br></li>
</ul>
<p><strong>Tvang mot samtykkekompetente pasienter</strong></p>
<ul>
<li>frihetsberøvelse, tvungen medisinering eller ernæring
<br><br></li>
</ul>
<p><strong>Tvangstiltak som ikke er helsehjelp</strong></p>
<ul>
<li>gjemme klær/låse skapdører (for å unngå roting)
<br><br></li>
</ul>
<p><strong>Ikke «nødvendig» helsehjelp, vesentlig helseskade eller den klart beste løsningen</strong></p>
<ul>
<li>svært langvarig bruk av brett i matsituasjoner, gjemme rullator for å motivere pasienten til å gå selv
<br><br></li>
</ul>
<p>Det er viktig å vurdere kritisk hvor nødvendig helsehjelpen er og identifisere alternativer til tvang. Ved å ikke dusje på noen uker, eller  at pasienten ikke tar en tablett innimellom vil ikke tvang være nødvendig. Her har kanskje leger kontra sykepleiere forskjellig syn. Derfor kan det være klokt i å ta kontakt med legen i disse spørsmålene.</p>
<p>Tverrfaglige diskusjoner med annet helsepersonell er i tillegg viktig fordi tvangsreglene forutsetter utstrakt bruk av skjønn.
<br><br></p>
<p><strong>HVORFOR FATTES IKKE VEDTAK?</strong></p>
<p>Mangel på tid ble oppgitt som en av de viktigste grunnene til at det ikke ble fattet vedtak. Det er utallige situasjoner der man burde ha fattet vedtak. Samtidig vurderer mange kravene til vedtak som altfor omfattende for de aktuelle tvangssituasjonene, spesielt hvis alle pleie- og omsorgssituasjoner skulle inkluderes.
<br><br></p>
<p><strong>Når skal man gi helsehjelp?</strong></p>
<p>En sentral term i kapittel 4A er «helsehjelp». Mye av det helsepersonell gir er nettopp helsehjelp, men «helsehjelp» er en term som ikke er særlig klar eller velavgrenset. Etter loven må helsehjelpen også være «nødvendig». Et annet krav i loven er at unnlatelse av helsehjelp kan føre til «vesentlig helseskade». Eksempel hvis sengen er full av avføring og urin er det da fare for helsa? eller kan det gi vesentelig helseskade?</p>
<p>Kapittel 4A gjelder kun for somatisk helsehjelp og en demensdiagnose, kan for noen oppfattes som om den faller inn under psykisk helsehjelp, men det gjør den ikke. Demensdiagnose faller inn under somatisk helsehjelp og faller da inn under kapittel 4A.</p>
