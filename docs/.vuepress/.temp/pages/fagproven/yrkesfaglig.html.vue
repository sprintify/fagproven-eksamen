<p><strong>Etisk dilemma</strong></p>
<p>Et etisk dilemma er en situasjon som er preget av tvil og usikkerhet og som handler om å foreta valg mellom to ubehagelige muligheter.
<br><br></p>
<p><em>Vi står overfor et etisk dilemma:</em></p>
<ul>
<li>Når ulike verdier er i konflikt med hverandre</li>
<li>Når det som er rett for én, er galt for en annen</li>
<li>Når alle tenkelige løsninger har minussider.
<br><br></li>
</ul>
<p><em>Når du står overfor et etisk dilemma, må du spørre deg selv:</em></p>
<ul>
<li>Hva skal jeg legge mest vekt på?</li>
<li>Skal jeg gjøre min plikt og følge skrevne og uskrevne lover og regler?</li>
<li>Skal jeg velge den løsningen som gir minst dårlige følger for meg selv og andre?
<br><br></li>
</ul>
<p><strong>Etikk</strong></p>
<p>En pliktetiker mener at menneskeverd og respekt står over hensynet til følgene av handlingene våre. En konsekvensetiker vil sette utfallet (det gode) over handlemåten (det rette).</p>
<p><em>For å finne ut om en avgjørelse er etisk forsvarlig, kan vi stille oss selv noen kontrollspørsmål:</em></p>
<ul>
<li>Er det lovlig?</li>
<li>Er det i samsvar med verdiene våre?</li>
<li>Er det riktig?</li>
<li>Beholder vi vår troverdighet?</li>
<li>Lønner det seg?</li>
<li>Lar det seg forsvare?
<br><br></li>
</ul>
<p><em>Et nøkkelspørsmål for å kontrollere valgene våre er dette:</em></p>
<p>– Tåler handlingene dagslys?</p>
<p>Måten å tenke på, og standpunktet vi tar, avhenger også av hvilken type etikk vi legger til grunn – pliktetikk eller konsekvensetikk.
<br><br></p>
