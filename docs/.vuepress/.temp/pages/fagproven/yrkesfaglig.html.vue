<p>Datteren som er eneste pårørende til sin mor gir klar beskjed til helsepersonell på sykehjemmet om å slutte å gi mor så mye medisiner på tvers av faglig råd fra f.eks. legen og sykepleiere. Moren trenger medisiner for å lindre smerte og angst.</p>
<p><em>Eksempel 2:</em></p>
<p>Pasient som ønsker å røyke, men får ikke lov, mens andre som er i samme situasjon får lov.</p>
<p><em>Eksempel 3:</em></p>
